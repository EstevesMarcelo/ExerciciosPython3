def aumentar(n):
    """

    :param n: número de análise
    :return: resultado da operação
    """
    return n * 1.1


def diminuir(n):
    """

    :param n: número de análise
    :return: resultado da operação
    """
    return n * 0.9


def dobro(n):
    """

    :param n: número de análise
    :return: resultado da operação
    """
    return n * 2


def metade(n):
    """

    :param n: número de análise
    :return: resultado da operação
    """
    return n/2


def moeda(n):
    """
    -> Função para conversão de unidades
    :param n: número de análise
    :return: retorna o valor convertido para forma monetária
    """
    return f'R${n}'
